        void load(const std::filesystem::path& file_path) {
            std::ifstream file(file_path, std::ios::ate);

            if (!file.is_open()) {
                status = Status::Valid;
                return;
            }

            std::streamsize size = file.tellg();
            file.seekg(0, std::ios::beg);
            content_buffer = std::make_unique<char[]>(size+1);

            bool read_load_failed = static_cast<bool>(file.read(content_buffer.get(), size));
            auto error_number = errno;
            file.close();

            if (error_number) {
                status = Status::Valid;
                return;
            }

            /*
             Assessing the file size in the as done above gives a byte count higher than what the file
            actualy has... Probably because of filesystem chunking scheme.
             To assess what the file's actual size is, the snippet bellow performs a binary search to determine
            the first 0x00 byte in the potentially oversized buffer.
            */
            size_t rside = size ;
            size_t lside = 0;

            while (lside < rside) {
                size_t mid = lside + ((rside - lside)>>1);
                if (content_buffer[mid] == 0) {
                    rside = mid;
                } else {
                    lside = mid + 1;
                }
            }

            content_size = lside+1; // +1 to "append" an extra 0x00. Needed for parsing.
            content = std::string_view(content_buffer.get(),content_size);
        }

